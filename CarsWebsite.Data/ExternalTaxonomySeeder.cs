    private record Row(string CategorySlug, string Brand, string Model, string Generation, int? YearFrom, int? YearTo);

    private static IEnumerable<Row> ParseRows()
    {
        foreach (var line in Rows)
        {
            var p = line.Split('|');
            if (p.Length != 6) continue;
            int? yf = int.TryParse(p[4], out var f) ? f : null;
            int? yt = int.TryParse(p[5], out var t) ? t : null;
            yield return new Row(p[0], p[1], p[2], p[3], yf, yt);
        }
    }

    // ASCII-only slug, matching the convention already used throughout Program.cs/ModelSeeder.cs
    // (e.g. "wozki-widlowe" for "Wozki widlowe") - transliterates Polish diacritics rather than
    // stripping them, so "Zoltek" and "Zółty" don't collide.
    private static string Slugify(string s)
    {
        var map = new Dictionary<char, string>
        {
            ['ą'] = "a", ['ć'] = "c", ['ę'] = "e", ['ł'] = "l", ['ń'] = "n",
            ['ó'] = "o", ['ś'] = "s", ['ź'] = "z", ['ż'] = "z",
            ['Ą'] = "a", ['Ć'] = "c", ['Ę'] = "e", ['Ł'] = "l", ['Ń'] = "n",
            ['Ó'] = "o", ['Ś'] = "s", ['Ź'] = "z", ['Ż'] = "z",
        };
        var sb = new StringBuilder();
        foreach (var ch in s)
        {
            if (map.TryGetValue(ch, out var repl)) sb.Append(repl);
            else if (char.IsLetterOrDigit(ch)) sb.Append(char.ToLowerInvariant(ch));
            else sb.Append('-');
        }
        var slug = sb.ToString();
        while (slug.Contains("--")) slug = slug.Replace("--", "-");
        return slug.Trim('-');
    }

    public static void Seed(AppDbContext db, ILogger logger)
    {
        var rows = ParseRows().ToList();
        if (rows.Count == 0) return;

        // Tracked (not AsNoTracking): brandsByName below loads Brand.Categories WITH tracking, so a
        // detached VehicleCategory instance here would collide with the already-tracked one in the
        // context's identity map the moment a new Brand's Categories list embeds it (EF throws
        // "cannot be tracked because another instance with the same key... is already being tracked").
        var categories = db.VehicleCategories.ToList()
            .GroupBy(c => c.Slug, StringComparer.OrdinalIgnoreCase)
            .ToDictionary(g => g.Key, g => g.First(), StringComparer.OrdinalIgnoreCase);

        // Keyed by lowercase name (brand) / "brand::model" / "brand::model::generation" so
        // newly-created-this-run entities (which have no Id yet) and pre-existing DB rows are
        // looked up the same way, instead of relying on FK values that aren't assigned until
        // SaveChanges.
        var brandsByName = db.Brands.Include(b => b.Categories).ToList()
            .GroupBy(b => b.Name, StringComparer.OrdinalIgnoreCase)
            .ToDictionary(g => g.Key, g => g.First(), StringComparer.OrdinalIgnoreCase);
        var modelsByKey = db.Models.Include(m => m.Brand).ToList()
            .GroupBy(m => $"{m.Brand.Name}::{m.Name}", StringComparer.OrdinalIgnoreCase)
            .ToDictionary(g => g.Key, g => g.First(), StringComparer.OrdinalIgnoreCase);
        var gensByKey = db.Generations.Include(g => g.Model).ThenInclude(m => m.Brand).ToList()
            .GroupBy(g => $"{g.Model.Brand.Name}::{g.Model.Name}::{g.Name}", StringComparer.OrdinalIgnoreCase)
            .ToDictionary(g => g.Key, g => g.First(), StringComparer.OrdinalIgnoreCase);

        int brandsAdded = 0, modelsAdded = 0, gensAdded = 0, skippedNoCategory = 0;

        foreach (var row in rows)
        {
            if (!categories.TryGetValue(row.CategorySlug, out var category))
            {
                skippedNoCategory++;
                logger.LogWarning("[TAXONOMY-IMPORT] Unknown category slug '{Slug}' for {Brand} {Model} - skipped", row.CategorySlug, row.Brand, row.Model);
                continue;
            }

            if (!brandsByName.TryGetValue(row.Brand, out var brand))
            {
                brand = new Brand { Name = row.Brand, Slug = Slugify(row.Brand), Categories = new List<VehicleCategory> { category } };
                db.Brands.Add(brand);
                brandsByName[row.Brand] = brand;
                brandsAdded++;
            }
            else if (!brand.Categories.Any(c => c.Id == category.Id))
            {
                brand.Categories.Add(category);
            }

            var modelKey = $"{row.Brand}::{row.Model}";
            if (!modelsByKey.TryGetValue(modelKey, out var model))
            {
                model = new Model { Brand = brand, Name = row.Model, Slug = $"{brand.Slug}-{Slugify(row.Model)}" };
                db.Models.Add(model);
                modelsByKey[modelKey] = model;
                modelsAdded++;
            }

            if (row.YearFrom.HasValue)
            {
                var genName = string.IsNullOrWhiteSpace(row.Generation) ? "Standard" : row.Generation;
                var genKey = $"{row.Brand}::{row.Model}::{genName}";
                if (!gensByKey.ContainsKey(genKey))
                {
                    var gen = new Generation
                    {
                        Model = model,
                        Name = genName,
                        YearFrom = row.YearFrom,
                        YearTo = row.YearTo,
                        Slug = $"{model.Slug}-{Slugify(genName)}-{row.YearFrom}",
                    };
                    db.Generations.Add(gen);
                    gensByKey[genKey] = gen;
                    gensAdded++;
                }
            }
        }

        db.SaveChanges();
        logger.LogWarning(
            "[TAXONOMY-IMPORT] ExternalTaxonomySeeder done: brands+={BrandsAdded} models+={ModelsAdded} generations+={GensAdded} skipped(unknown category)={SkippedNoCategory} (total rows processed: {Total})",
            brandsAdded, modelsAdded, gensAdded, skippedNoCategory, rows.Count);
    }
